# list  same as array in other programming languages
# list is a collection of items in a particular order

friends = ["Anand", "Raj", "Bind", 1, 2, 3, True] # list can contain different data types
print(friends[4]) # accessing the first element of the list


friends[0] = "Aman" #unlike string list is mutable we can change the value of the list
print(friends)
#  same as string slicing is allow in the list
print(friends[1:4])


